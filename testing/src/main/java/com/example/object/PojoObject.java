package com.example.object;

public class PojoObject {
	private String longName;
	private String shortName;
	private String viscosity;
	private String packing;
	private String boxContents;
	private String wholesalerPrice;
	private String srp;
	
	public String getLongName() {
		return longName;
	}
	public void setLongName(String longName) {
		this.longName = longName;
	}
	public String getShortName() {
		return shortName;
	}
	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	public String getViscosity() {
		return viscosity;
	}
	public void setViscosity(String viscosity) {
		this.viscosity = viscosity;
	}
	public String getPacking() {
		return packing;
	}
	public void setPacking(String packing) {
		this.packing = packing;
	}
	public String getBoxContents() {
		return boxContents;
	}
	public void setBoxContents(String boxContents) {
		this.boxContents = boxContents;
	}
	public String getWholesalerPrice() {
		return wholesalerPrice;
	}
	public void setWholesalerPrice(String wholesalerPrice) {
		this.wholesalerPrice = wholesalerPrice;
	}
	public String getSrp() {
		return srp;
	}
	public void setSrp(String srp) {
		this.srp = srp;
	}
	
	
}
